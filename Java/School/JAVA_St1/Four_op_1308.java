public class Four_op_1308 {
	public static void main ( String [ ] args ) {
		double number = 3.5, number1 = 7.2;
		System.out.println(number + " + " + number1 + " = " + (number + number1));
		System.out.println(number + " - " + number1 + " = " + (number - number1));
		System.out.println(number + " * " + number1 + " = " + (number * number1));
		System.out.println(number + " / " + number1 + " = " + (number / number1));

	}
}